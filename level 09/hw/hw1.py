#) შექმენით შემდეგი ცვლადები: name, age, height(სახელი, ასაკი, სიმაღლე), სადაც შევინახავთ სხვადასხვა ტიპის მნიშვნელობებს.
#საბოლოოდ დაპრინტეთ ამ ცვლადების ტიპები ფუნქცია type()-ის გამოყენებით.

name = "kesane" #stores a str
age = 7 #stores an int
height = 1.23 #stores a float
print(type(name)) #outputs <class 'str'>
print(type(age)) #outputs <class 'int'>
print(type(height)) #outputs <class 'float'>