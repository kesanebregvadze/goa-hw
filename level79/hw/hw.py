# #1) დაწერე ფუნქცია, რომელიც მიიღებს ფერს ("წითელი", "ყვითელი", "მწვანე") და დაბეჭდოს შესაბამისი ტექსტი:

# "წითელი" → "გაჩერდი!"

# "ყვითელი" → "მოემზადე!"

# "მწვანე" → "გაიარე!"

# სხვა შემთხვევაში → "შეცდომა: უცნობი ფერი!"
inp=input("შეიყვანე ერთი ფერი:")
def fun():
    if inp == "წითელი":
        return "გაჩერდი!"
    elif inp == "ყვითელი":
        return "მოემზადე!"
    elif inp == "მწვანე":
        return "გაიარე!"
    else:
        return "შეცდომა: უცნობი ფერი!"
print(fun())