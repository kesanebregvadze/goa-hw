
# 2) დაწერე ფუნქცია, რომელიც მიიღებს ქულას (0-100) და დაბეჭდოს შეფასება:

# 90-100 → "შესანიშნავი!"

# 75-89 → "კარგი!"

# 50-74 → "საშუალო!"

# 30-49 → "სუსტია!"

# 0-29 → "ჩავარდნა!"
def fun(qula):
    if 90 <= qula <= 100:
        return"შესანიშნავი!"
    elif 75 <= qula <= 89:
        return"კარგი!"
    elif 50 <= qula <= 74:
        return"საშუალო!"
    elif 30 <= qula <= 49:
        return"სუსტია!"
    elif 0 <= qula <= 29:
        return"ჩავარდნა!"
print(fun(97))