# 3) დაწერე ფუნქცია, რომელიც მიიღებს საათების რაოდენობას და დააბრუნებს პარკინგის გადასახადს:

# 1 საათამდე → "უფასო"

# 1-3 საათი → "5 ლარი"

# 3-6 საათი → "10 ლარი"

# 6+ საათი → "20 ლარი"
def fun(h):
    if 0 <= h <= 1:
        return "უფასო"
    elif 1 <= h <= 3:
        return "5 ლარი"
    elif 3 <= h <= 6:
        return "10 ლარი"
    elif h > 6:
        return "20 ლარი"
print(fun(9))
